# Lista de dicionários com informações de pessoas
pessoas = [
    {'nome': 'Ana', 'idade': 30, 'cidade': 'São Paulo'},
    {'nome': 'João', 'idade': 25, 'cidade': 'Rio de Janeiro'},
    {'nome': 'Maria', 'idade': 35, 'cidade': 'Belo Horizonte'},
    {'nome': 'Pedro', 'idade': 28, 'cidade': 'Salvador'}
]

# Iterando sobre a lista de dicionários e imprimindo as informações de cada pessoa
for pessoa in pessoas:
    print(f"Nome: {pessoa['nome']}, Idade: {pessoa['idade']}, Cidade: {pessoa['cidade']}")
