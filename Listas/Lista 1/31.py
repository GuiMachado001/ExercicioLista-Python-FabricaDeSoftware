# 31. Leia um valor em real e a cotação do dólar. Em seguida, imprima o valor correspondente em
# dólares


valorDolar = float(input("Digite o valor atual da cotação do dolar: "))

valorReal = float(input("Digite o valor que deseja converter para Dolar: "))

res = valorReal * valorDolar

print("O valor em Dolar é: ",res,"$")