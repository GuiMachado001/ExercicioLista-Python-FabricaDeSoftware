# Escreva um programa que leia um inteiro entre 1 e 12 e imprima o mês correspondente a este 
# número. Isto e, janeiro se é 1, fevereiro é 2, e assim por diante.

num = int(input("Digite um número entre 1 e 12: "))

if(num == 1):
    print("Mês equivalente à: Janeiro")
elif(num == 2):
    print("Mês equivalente à: Fevereiro")
elif(num == 3):
    print("Mês equivalente à: Março")
elif(num == 4):
    print("Mês equivalente à: Abril")
elif(num == 5):
    print("Mês equivalente à: Maio")
elif(num == 6):
    print("Mês equivalente à: Junho")
elif(num == 7):
    print("Mês equivalente à: Julho")
elif(num == 8):
    print("Mês equivalente à: Agosto")
elif(num == 9):
    print("Mês equivalente à: Setembro")
elif(num == 10):
    print("Mês equivalente à: Outubro")
elif(num == 11):
    print("Mês equivalente à: Novembro")
elif(num == 12):
    print("Mês equivalente à: Dezembro")